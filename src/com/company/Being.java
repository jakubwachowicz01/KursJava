package com.company;

import java.util.Random;

public abstract class Being {
    private String name;
    private int power;
    private int defense;
    private int coins;
    Random random;

    public Being() {
        this.name = name;
        this.power = power;
        this.defense = defense;
        this.coins = coins;
        this.random = random;
    }

}
